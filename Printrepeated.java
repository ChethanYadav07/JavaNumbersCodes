package com.logical.pkg4;

public class Printrepeated
{
	public static void test()
	{
		int res=1;
		int n=50;
		System.out.println(res);
		for(int i=2;i<=n;i++)
		{
			res=res+i;
			System.out.println(res);
		}
	}
	public static void main(String[] args) 
	{
		
          test();
	}

}
